public readonly struct DamageValues
{
    public abstract class EnemyDamage
    {
        
        public const float gruntDamage = 15;
        public const float spikeDamage = 50;
        public const float bossDamage = 30;
    }

    public abstract class PlayerDamage
    {
        public const float noItemDamage = 3;
        public const float daggerDamage = 15;
    }


}
